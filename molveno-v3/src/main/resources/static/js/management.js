function openTab(ev, arg) {
    if (arg == 'Dishes') {
        window.open("../editDishes.html", "_self");
    }
    if (arg == 'Specials') {
        window.open("../editSpecials.html", "_self");
    }
    if (arg == 'Stock') {
        window.open("../stock.html", "_self");
    }
    if (arg == 'Financial') {
        window.open("../financial.html", "_self");
    }
    if (arg == 'Management') {
        window.open("../management.html", "_self");
    }
}